Create Database OXXO

use OXXO

-- CREACION DE UN NUEVA TABLA
create table Clientes(
	 Id_cliente VARCHAR(10) NOT NULL PRIMARY KEY,
	 Nombre VARCHAR(50) NOT NULL,
	 Direccion VARCHAR(50) NOT NULL,
	 Telefono varchar(50) not null,
	 Ciudad VARCHAR(50) not null
	 );	 

-- CREACION DE UN NUEVA TABLA
CREATE TABLE Productos(
	Id_producto int NOT NULL  PRIMARY KEY,
	Descripcion VARCHAR(200) NOT NULL,
	Precio NUMERIC(10) NOT NULL
); 

-- CREACION DE UN NUEVA TABLA
CREATE TABLE Ventas(
	Id_venta int NOT NULL PRIMARY KEY,
	Cantidad  int   NOT NULL,
	Id_cliente varchar(10) NOT NULL REFERENCES Clientes,
	Id_producto int NOT NULL REFERENCES Productos
);

